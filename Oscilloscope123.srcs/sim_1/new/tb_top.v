`timescale 1ns/1ps

module tb_top;

    //====================================================
    // 1. ЪфШыаХКХ
    //====================================================
    reg clk;
    reg rst_n;
    reg [1:0] mode;
    reg [2:0] freq_sel;

    //====================================================
    // 2. ЪфГіаХКХ
    //====================================================
    wire [7:0] wave_out;
    wire pwm_out;

    //====================================================
    // 3. МђЛЏRCТЫВЈКѓЕФЙлВьСП
    // ЫЕУїЃК
    // етВЛЪЧбЯИёФЃФтЕчТЗЃЌжЛЪЧЮЊСЫдкЗТецРяПД"ЦНЛЌЧїЪЦ"
    //====================================================
    real v_rc;

    //====================================================
    // 4. ЪЕР§ЛЏЖЅВуФЃПщ
    //====================================================
    top uut (
        .clk(clk),
        .rst_n(rst_n),
        .mode(mode),
        .freq_sel(freq_sel),
        .wave_out(wave_out),
        .pwm_out(pwm_out)
    );

    //====================================================
    // 5. 100MHz ЪБжг
    // жмЦк = 10ns
    //====================================================
    initial begin
        clk = 1'b0;
    end

    always #5 clk = ~clk;

    //====================================================
    // 6. RCТЫВЈаЇЙћЕФааЮЊМЖНќЫЦ
    // pwm_out=1 ЪБЯђ 1.0 ППНќ
    // pwm_out=0 ЪБЯђ 0.0 ППНќ
    //====================================================
    initial begin
        v_rc = 0.0;
    end

    always @(posedge clk) begin
        if (pwm_out)
            v_rc = v_rc + 0.02 * (1.0 - v_rc);
        else
            v_rc = v_rc + 0.02 * (0.0 - v_rc);
    end

    //====================================================
    // 7. ЗТецМЄРј
    //====================================================
    initial begin
        // ГѕЪМжЕ
        rst_n    = 1'b0;
        mode     = 2'b00;
        freq_sel = 3'd0;

        // БЃГжИДЮЛвЛЖЮЪБМф
        #20;
        rst_n = 1'b1;

        //================================================
        // ЯШВте§ЯвВЈЃЈDDSЃЉ
        //================================================

        // е§ЯвВЈ 100HzЕЕ
        mode     = 2'b10;
        freq_sel = 3'd0;
        #1000000;

        // е§ЯвВЈ 1kHzЕЕ
        freq_sel = 3'd2;
        #1000000;

        // е§ЯвВЈ 10kHzЕЕ
        freq_sel = 3'd5;
        #1000000;

        //================================================
        // дйВтЗНВЈ
        //================================================
        mode     = 2'b00;
        freq_sel = 3'd0;
        #1000000;

        //================================================
        // дйВтШ§НЧВЈ
        //================================================
        mode     = 2'b01;
        freq_sel = 3'd0;
        #1000000;

        $stop;
    end

endmodule